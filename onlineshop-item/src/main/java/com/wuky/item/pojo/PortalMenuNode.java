package com.wuky.item.pojo;

import java.util.List;


/**
 * 每个节点类型
 */
public class PortalMenuNode
{
	private String u;
	private String n;
	private List<Object> i;

	public String getU()
	{
		return u;
	}

	public void setU(final String u)
	{
		this.u = u;
	}

	public String getN()
	{
		return n;
	}

	public void setN(final String n)
	{
		this.n = n;
	}

	public List<Object> getI()
	{
		return i;
	}

	public void setI(final List<Object> i)
	{
		this.i = i;
	}


}
